/// Преобразует два 16-битных слова (старшее и младшее) в 32-битное число с плавающей точкой.
///
/// Modbus-устройства часто передают 32-битные float как два последовательных 16-битных регистра.
/// Эта функция объединяет их в одно значение `f32`.
///
/// # Аргументы
///
/// * `hi` — старшие 16 бит (первый регистр).
/// * `lo` — младшие 16 бит (второй регистр).
///
/// # Возвращает
///
/// 32-битное число с плавающей точкой (`f32`).
pub fn words_to_f32(hi: u16, lo: u16) -> f32 {
    // Объединяем два 16-битных слова в 32-битное целое
    let bits = ((hi as u32) << 16) | (lo as u32);

    // Интерпретируем биты как f32 (reinterpret cast)
    f32::from_bits(bits)
}
